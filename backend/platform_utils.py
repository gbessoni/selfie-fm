"""
Platform detection utility for social media links
Detects the platform from URL and returns the appropriate identifier
"""
from urllib.parse import urlparse

def detect_platform(url: str) -> str:
    """
    Detect the platform from a URL
    
    Args:
        url: The URL to analyze
        
    Returns:
        Platform identifier (e.g., 'instagram', 'facebook', 'twitter', 'website')
    """
    if not url:
        return 'website'
    
    try:
        # Parse the URL to get the domain
        parsed = urlparse(url.lower())
        domain = parsed.netloc or parsed.path
        
        # Remove www. prefix
        domain = domain.replace('www.', '')
        
        # Platform detection mapping
        platform_mapping = {
            'instagram.com': 'instagram',
            'facebook.com': 'facebook',
            'fb.com': 'facebook',
            'twitter.com': 'twitter',
            'x.com': 'twitter',
            'linkedin.com': 'linkedin',
            'youtube.com': 'youtube',
            'youtu.be': 'youtube',
            'tiktok.com': 'tiktok',
            'spotify.com': 'spotify',
            'music.apple.com': 'apple-music',
            'soundcloud.com': 'soundcloud',
            'twitch.tv': 'twitch',
            'github.com': 'github',
        }
        
        # Check if domain matches any known platform
        for platform_domain, platform_name in platform_mapping.items():
            if platform_domain in domain:
                return platform_name
        
        # Default to website for unknown URLs
        return 'website'
        
    except Exception:
        return 'website'


